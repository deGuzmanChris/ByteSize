async function importData() {
  console.log('üöÄ Starting data import...\n');

  // Read the JSON file
  const data = JSON.parse(fs.readFileSync('./firebase_seed_data.json', 'utf8'));

  let totalImported = 0;

  // Import each collection
  for (const [collectionName, documents] of Object.entries(data)) {
    console.log(`üì¶ Importing collection: ${collectionName}`);
    
    for (const [docId, docData] of Object.entries(documents)) {
      try {
        // If the document has an empty 'id' field, set it to the document ID
        if (docData.id === '') {
          docData.id = docId;
        }

        await db.collection(collectionName).doc(docId).set(docData);
        console.log(`  ‚úì Created document: ${docId}`);
        totalImported++;
      } catch (error) {
        console.error(`  ‚úó Error creating document ${docId}:`, error);
      }
    }
    
    console.log('');
  }

  console.log(`‚úÖ Import complete! Total documents imported: ${totalImported}`);
}

